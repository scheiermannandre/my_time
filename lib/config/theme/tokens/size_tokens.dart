/// A utility class providing predefined size tokens for consistent spacing.
class SizeTokens {
  /// Standard size token representing 8.0 logical pixels.
  static const x8 = 8.0;

  /// Standard size token representing 24.0 logical pixels.
  static const x24 = 24.0;

  /// Standard size token representing 48.0 logical pixels.
  static const x48 = 48.0;

  /// Standard size token representing 72.0 logical pixels.
  static const x72 = 72.0;
}
